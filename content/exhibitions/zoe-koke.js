  text: 'Show globes have a long history. This project began with a photo I took of show globes in New Orleans at the oldest pharmacy in the United States. When planning for this installation, I placed this image on a photograph of Washer / Dryer Projects which was then hung in the space for this exhibit. Show globes are large glass vessels filled with colorful liquid which were stationed in pharmacy windows to warn citizens of town illnesses throughout the advent of the pharmacy. This form of communication for an illiterate public traveled from 17th century England to the United States. Red and blue liquids may have represented venous and arterial blood, signalling warning. Green liquid may have suggested that all was well. Meanwhile as pharmacies cropped up in the southern states, Voodoo medicines, herb based tried and true remedies, were sold on the sly by pharmacies to the wealthy while harmful concoctions like mercury were peddled proudly.<br><br>It seems fitting to present these images in Washer / Dryer Projects. Pink liquid in cheap champagne glasses, an offering or warning to guests who will not visit the installation physically. A laundry room is a site of domestic labour that is largely invisible to a general public, while the home and its construction has traditionally been a measure of conventional success. Washer / Dryer Projects is an alternative art space which meets its viewers through its photographic documentation, as all artworks presented here undergo different levels of translation and find their final formation as photographs.<br><br>The photos in American Myth were made between now and my move to the U.S. in 2017. They are images made by an onlooker, fed a lifetime of American symbolism, without much access to a correlating physical geography. Replicas of the Statue of Liberty recur: as recreated in Vegas, and again made in paper maché, carried for a protest in New Orleans after the 2016 election. Meanwhile a white horse at the entry of a marriage parlor in Vegas is posited onto a misty anti-monumental image of the Grand Canyon. Compiling and merging these images and growing an archive of photographs from the U.S. represents my fascination with the construction of the American dream as well as its maintenance through images, symbols, places and icons. I am working on a book to continue this project.<br><br>Zoe Koke,( b, 1989, Calgary) recently graduated with her MFA from UCLA. She holds a BFA from Concordia University, Montreal. Recent exhibitions include “Celebration”, Insect Gallery, Los Angeles (2019); The Butterfly Effect, SPACE, Vancouver (2019); 22nd Century Woman, Moma PS311, Ottawa (2019); By and By, Durden and Ray, Los Angeles (2018); Alligator, Club Lake, Texas, curated by Partial Shade (2018); Made to Look Natural (with Ben Borden) Smart Objects, Landers, Ca (2018). Koke will participate in upcoming exhibitions at Ochi Projects, Los Angeles  and PSLA, Los Angeles.',
